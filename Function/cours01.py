def secteur(texte): #fonction avec un paramètre
    print("-"*30) #quantité de fois que le "-" sera répeter
    print(texte) #Le paramètre qui on a indiqué dans la fonction
    print("-"*30)


secteur("   Systeme comptable   ")#On appelle la fonction, on passe le texte au paramètre "texte" de la fonction
secteur("   États financiers    ")
secteur("   Comptes a payer     ")

